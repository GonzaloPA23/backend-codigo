a = 5
b = 10

# Operadores aritméticos
print(a + b) # -> suma
print(a - b) # -> resta
print(a * b) # -> multiplicación
print(a / b) # -> división
print(a % b) # -> módulo
print(a ** b) # -> exponente
print(a // b) # -> división entera

# Operadores de comparación
print(a == b) # -> igual
print(a != b) # -> diferente
print(a > b) # -> mayor que
print(a < b) # -> menor que
print(a >= b) # -> mayor o igual que
print(a <= b) # -> menor o igual que

# Operadores logicos
print(a > 3 and b < 11) # -> and
print(a > 3 or b < 11) # -> or
print(not(a > 3 and b < 11)) # -> not

